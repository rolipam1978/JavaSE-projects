
package srdm;

/**
 *
 * @author Ramiro
 */
public class Bean {
    
    private Integer id;
    private String data;
    private String enfermaria;
    private Short motivo;

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    /**
     * @return the enfermaria
     */
    public String getEnfermaria() {
        return enfermaria;
    }

    /**
     * @param enfermaria the enfermaria to set
     */
    public void setEnfermaria(String enfermaria) {
        this.enfermaria = enfermaria;
    }

    /**
     * @return the motivo
     */
    public Short getMotivo() {
        return motivo;
    }

    /**
     * @param motivo the motivo to set
     */
    public void setMotivo(Short motivo) {
        this.motivo = motivo;
    }
    
    
    
}
